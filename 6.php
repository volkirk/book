«Мероприятие Х» заканчивается в 22.00. Я выпиваю «Фервекс Упсу». Потею, температура падает до 37,3. Я собираюсь, глажу рубашку, умываюсь, чищу зубы, морально готовлюсь к тому, что нужно сойти за здорового.
