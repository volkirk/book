Часто женщина вскакивает. И это нормальная ожидаемая реакция.
Самое главное — никуда её не отпускать.
Я обычно говорю: «Хорошо, успокойся, всё нормально. Все так делают, всё хорошо!»
Да, другой мужик может вызвать у неё шок. Нужно успокоить её, если будут слёзы — не беда, проплачется и успокоится и через полчаса будет ловить кайф. Самое главное — не действовать против её воли. Нужно, чтобы женщина понимала: вы занимаетесь этим потому, что хотите доставить удовольствие себе и ей. А не потому, что она шлюха!
8. Когда мой друг входит, я никогда не отпускаю свою подругу. Я не должен выйти из неё. И в 70% случаев у меня это получается. Тогда мой друг ложится и гладит её нежно. Он должен сказать фразу, чтобы она расслабилась: «Я просто посмотрю, ничего делать не буду», если видит или слышит, или чувствует, что она напрягается.
9. Правило полного контакта. Когда мы лежим и она уже нормально воспринимает, что он гладит её и трогает за грудь, он должен уверенным движением пробраться к клитору и начать его массировать. Презерватив он должен натянуть до этого так, чтобы она не видела. Когда он массирует клитор, девушка привыкает к тому, что два мужчины действуют слаженно, и настраивается на групповой секс неосознанно. Она уже возбуждается не только от члена её парня, но и от рук друга. Тогда, когда я чувствую, что она уже успокоилась и расслабилась и опять чувствует кайф, я должен подать другу знак. Обычно я щипаю его. Женщина не должна заметить этого. Всё должно выглядеть естественно. Когда я его щипаю, он понимает, что уже пора входить. Главное, он должен запустить в неё свои пальцы, пока я сам нахожусь в ней. Это так важно потому, что если она почувствует, что внутри неё никого нет, её мозг тут же молниеносно переключится. У меня уже было такое. И тогда шансов потрахаться у друга уже меньше. Поэтому, когда я достаю свой член, его пальцы ещё внутри, и он имеет возможность контролировать её, активно двигая пальцами, возбуждая её, не давая ей ни на миллисекунду остыть.
И чем быстрее, тем лучше, друг должен ей вставить.
