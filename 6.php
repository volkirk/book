5. Возбудить обеих одновременно.
6. Настроить их на конкуренцию.
7. Создать особую связь с каждой в отдельности, следить за тем, чтобы между ними не было никакой связи.
8. Уединиться.
9. Настроить одну из них на сексуальные действия по отношению к другой.
10. Настроить одну из них раздеть другую, просто поприкалываться над ней.
11. Настроить другую раздеть в отместку первую.
12. Переспать с одной из них, потом с другой.
Если это быстрое соблазнение на вечеринке, где ты случайно с двумя дамами забрёл в комнату и вообще в первый раз их видишь, есть один принцип. Если они хорошо относятся друг к другу, соврати их на секс друг с другом, на то, чтобы они трогали друг друга. Действуй, бери руки одной, клади на ноги другой и т. п. Чтобы они перестали понимать, кто кого трогает, и у них ехала крыша. Постоянно контролируй два женских тела, проявляй настойчивость и войди в одну из них. Не давай им говорить серьёзно, щекочи их, шути с ними, разденься догола, раздень их, веди себя как животное, и у них уедет крыша, и они рано или поздно отдадутся тебе.
Руководи ими властно!
